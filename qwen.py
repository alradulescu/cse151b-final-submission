def validate_competition_model(model: str) -> None:
    """Allow only the designated base model or LoRA adapters derived from it."""
    if model == DESIGNATED_MODEL:
        return

    model_path = Path(model)
    adapter_config = model_path / "adapter_config.json"
    if adapter_config.exists():
        try:
            config = json.loads(adapter_config.read_text(encoding="utf-8"))
        except json.JSONDecodeError as exc:
            raise ValueError(f"Invalid adapter_config.json for {model}: {exc}") from exc
        if config.get("base_model_name_or_path") == DESIGNATED_MODEL:
            return

    raise ValueError(
        "qwen.py may only use Qwen/Qwen3-4B-Thinking-2507 or a LoRA adapter "
        "whose adapter_config.json names that base model. "
        f"Got: {model}"
    )


def load_jsonl(path: Path) -> list[dict]:
    if not path.exists():
        raise FileNotFoundError(path)
    return [json.loads(line) for line in path.read_text(encoding="utf-8").splitlines() if line.strip()]


def load_by_id(path: Path) -> dict[int, dict]:
    return {int(row["id"]): row for row in load_jsonl(path)}


def write_jsonl(path: Path, rows: list[dict]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp_path = path.with_suffix(path.suffix + ".tmp")
    with tmp_path.open("w", encoding="utf-8") as handle:
        for row in rows:
            handle.write(json.dumps(row, ensure_ascii=False) + "\n")
    tmp_path.replace(path)


def load_id_filter(ids: str, ids_file: Optional[Path]) -> set[int] | None:
    raw_values: list[str] = []
    if ids:
        raw_values.extend(part.strip() for part in ids.split(","))
    if ids_file is not None:
        for line in ids_file.read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if not line or line.startswith("#"):
                continue
            raw_values.extend(part.strip() for part in line.split(","))
    values = [value for value in raw_values if value]
    return {int(value) for value in values} if values else None


def format_options(options: Optional[list]) -> str:
    if not options:
        return ""
    labels = [chr(65 + idx) for idx in range(len(options))]
    return "\n".join(f"{label}. {str(option).strip()}" for label, option in zip(labels, options))


def trim_previous_response(response: str, base_tail_chars: int) -> str:
    response = response or ""
    if base_tail_chars > 0 and len(response) > base_tail_chars:
        return response[-base_tail_chars:]
    return response


def load_candidate_results(specs: list[str]) -> dict[str, dict[int, dict]]:
    candidates: dict[str, dict[int, dict]] = {}
    for spec in specs:
        if "=" not in spec:
            raise ValueError(
                "--candidate-results entries must be name=path, "
                f"got {spec!r}"
            )
        name, raw_path = spec.split("=", 1)
        name = name.strip()
        if not name:
            raise ValueError(f"Empty candidate name in {spec!r}")
        path = Path(raw_path)
        if not path.exists():
            print(f"WARNING: skipping missing candidate result {name}={path}", file=sys.stderr)
            continue
        candidates[name] = load_by_id(path)
    return candidates


def format_candidate_block(
    item_id: int,
    previous_row: dict,
    candidate_results: dict[str, dict[int, dict]],
    candidate_tail_chars: int,
) -> str:
    rows: list[tuple[str, dict]] = [("base", previous_row)]
    rows.extend(
        (name, by_id[item_id])
        for name, by_id in candidate_results.items()
        if item_id in by_id
    )
    chunks = []
    seen: set[str] = set()
    for name, row in rows:
        response = trim_previous_response(str(row.get("response", "")), candidate_tail_chars)
        if not response.strip():
            continue
        key = response.strip()
        if key in seen:
            continue
        seen.add(key)
        chunks.append(f"Candidate {name}:\n{response}")
    if chunks:
        return "\n\n".join(chunks)
    return r"Candidate base:\n\boxed{}"


