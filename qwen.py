#!/usr/bin/env python3
"""Legal Qwen-only boxed-answer pass.

This module is intentionally narrow:

- it uses only ``Qwen/Qwen3-4B-Thinking-2507`` or a LoRA adapter whose
  ``adapter_config.json`` names that exact base model;
- it runs an additional model generation pass over an existing model response;
- it renders Qwen chat prompts with thinking disabled by default;
- it does not call solvers, calculators, symbolic math, external APIs, web
  services, or deterministic answer-computing repair code.

The pass may reformat, extract, verify, or repair by asking the allowed Qwen
model itself. Any final answer selection comes from model output, not Python
math.
"""

