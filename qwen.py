def render_prompt(tokenizer, messages: list[dict], disable_thinking: bool = True) -> str:
    kwargs = {
        "tokenize": False,
        "add_generation_prompt": True,
        "enable_thinking": not disable_thinking,
    }
    try:
        rendered = tokenizer.apply_chat_template(messages, **kwargs)
    except TypeError:
        kwargs.pop("enable_thinking", None)
        rendered = tokenizer.apply_chat_template(messages, **kwargs)
    if disable_thinking and rendered.rstrip().endswith("<think>"):
        rendered = rendered.rstrip() + "\n</think>\n\n"
    return rendered


def extract_last_boxed(text: str) -> str | None:
    marker = r"\boxed{"
    idx = text.rfind(marker)
    if idx < 0:
        return None
    i = idx + len(marker)
    depth = 1
    out: list[str] = []
    while i < len(text) and depth > 0:
        ch = text[i]
        if ch == "{":
            depth += 1
            out.append(ch)
        elif ch == "}":
            depth -= 1
            if depth:
                out.append(ch)
        else:
            out.append(ch)
        i += 1
    if depth:
        return None
    return "".join(out).strip()


def coerce_boxed_response(response: str) -> str:
    """Box the model's own final string without computing or choosing answers."""
    response = (response or "").strip()
    boxed = extract_last_boxed(response)
    if boxed is not None:
        return rf"\boxed{{{boxed}}}"
    if not response:
        return r"\boxed{}"
    for prefix in ("Final answer:", "Final Answer:", "Answer:", "The answer is"):
        if response.startswith(prefix):
            response = response[len(prefix) :].strip()
    return rf"\boxed{{{response}}}"


def apply_boxed_prefill(prompt: str) -> str:
    return prompt + r"\boxed{"


def restore_boxed_prefill(response: str) -> str:
    response = (response or "").strip()
    if response.startswith(r"\boxed{"):
        restored = response
    else:
        restored = r"\boxed{" + response
    if restored.startswith(r"\boxed{") and restored.count("{") == restored.count("}") + 1:
        restored += "}"
    return restored


def load_transformers_model(model_name: str, gpu_id: str | None):
    if gpu_id is not None:
        os.environ["CUDA_VISIBLE_DEVICES"] = gpu_id
    os.environ.setdefault("PYTORCH_CUDA_ALLOC_CONF", "expandable_segments:True")

    import torch
    from transformers import AutoModelForCausalLM, AutoTokenizer, BitsAndBytesConfig

    bnb_config = BitsAndBytesConfig(
        load_in_4bit=True,
        bnb_4bit_quant_type="nf4",
        bnb_4bit_compute_dtype=torch.bfloat16,
        bnb_4bit_use_double_quant=True,
    )

    adapter_config = Path(model_name) / "adapter_config.json"
    if adapter_config.exists():
        from peft import PeftModel

        config = json.loads(adapter_config.read_text(encoding="utf-8"))
        base_model_name = config["base_model_name_or_path"]
        try:
            tokenizer = AutoTokenizer.from_pretrained(model_name, trust_remote_code=True)
        except Exception:
            tokenizer = AutoTokenizer.from_pretrained(base_model_name, trust_remote_code=True)
        base_model = AutoModelForCausalLM.from_pretrained(
            base_model_name,
            trust_remote_code=True,
            quantization_config=bnb_config,
            device_map="auto",
            attn_implementation="eager",
        )
        model = PeftModel.from_pretrained(base_model, model_name).merge_and_unload()
    else:
        tokenizer = AutoTokenizer.from_pretrained(model_name, trust_remote_code=True)
        model = AutoModelForCausalLM.from_pretrained(
            model_name,
            trust_remote_code=True,
            quantization_config=bnb_config,
            device_map="auto",
            attn_implementation="eager",
        )

    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token
    tokenizer.padding_side = "left"
    model.eval()
    return model, tokenizer


def load_vllm_model(model_name: str, args):
    if args.gpu_id is not None:
        os.environ["CUDA_VISIBLE_DEVICES"] = args.gpu_id

    from transformers import AutoTokenizer
    from vllm import LLM
    from vllm.lora.request import LoRARequest

    adapter_config = Path(model_name) / "adapter_config.json"
    lora_request = None
    if adapter_config.exists():
        config = json.loads(adapter_config.read_text(encoding="utf-8"))
        load_model_name = config["base_model_name_or_path"]
        try:
            tokenizer = AutoTokenizer.from_pretrained(model_name, trust_remote_code=True)
        except Exception:
            tokenizer = AutoTokenizer.from_pretrained(load_model_name, trust_remote_code=True)
        lora_request = LoRARequest("qwen_pass_adapter", 1, str(Path(model_name)))
    else:
        load_model_name = model_name
        tokenizer = AutoTokenizer.from_pretrained(model_name, trust_remote_code=True)

    if tokenizer.pad_token is None:
        tokenizer.pad_token = tokenizer.eos_token
    tokenizer.padding_side = "left"

    llm = LLM(
        model=load_model_name,
        trust_remote_code=True,
        dtype=args.dtype,
        max_model_len=args.max_model_len,
        gpu_memory_utilization=args.gpu_memory_utilization,
        max_num_seqs=args.vllm_max_num_seqs,
        max_num_batched_tokens=args.vllm_max_num_batched_tokens,
        enable_lora=lora_request is not None,
        max_lora_rank=args.vllm_max_lora_rank,
    )
    return llm, tokenizer, lora_request


def select_items(args) -> tuple[list[dict], dict[int, dict]]:
    items = load_jsonl(args.data)
    base_by_id = load_by_id(args.base_results)
    if args.question_filter != "all":
        want_mcq = args.question_filter == "mcq"
        items = [item for item in items if bool(item.get("options")) == want_mcq]
    id_filter = load_id_filter(args.ids, args.ids_file)
    if id_filter is not None:
        items = [item for item in items if int(item["id"]) in id_filter]
    items = [item for item in items if int(item["id"]) in base_by_id]
    if args.start_index > 0:
        items = items[args.start_index :]
    if args.limit > 0:
        items = items[: args.limit]
    return items, base_by_id


def run_qwen_pass(args) -> int:
    validate_competition_model(args.model)
    items, base_by_id = select_items(args)
    candidate_results = load_candidate_results(args.candidate_results)
    if args.mode == "choose" and not candidate_results:
        print(
            "WARNING: --mode choose has no available candidate results; "
            "falling back to Qwen verification of --base-results only.",
            file=sys.stderr,
        )

    existing: list[dict] = []
    done_ids: set[int] = set()
    if args.resume and args.output.exists():
        existing = load_jsonl(args.output)
        done_ids = {int(row["id"]) for row in existing}
    pending = [item for item in items if int(item["id"]) not in done_ids]
    print(f"Loaded {len(items)} eligible items; pending {len(pending)}", flush=True)
    if not pending:
        return 0

    if args.backend == "vllm":
        from vllm import SamplingParams

        model, tokenizer, lora_request = load_vllm_model(args.model, args)
        sampling_params = SamplingParams(
            max_tokens=args.max_tokens,
            temperature=0.0,
            seed=42,
            stop=["\n", "<|im_end|>"] if args.boxed_prefill else None,
        )
    else:
        model, tokenizer = load_transformers_model(args.model, args.gpu_id)
        sampling_params = None
        lora_request = None

    all_rows = list(existing)
    for start in tqdm(range(0, len(pending), args.batch_size), desc="Qwen pass"):
        batch = pending[start : start + args.batch_size]
        prompts = [
            render_prompt(
                tokenizer,
                build_messages(
                    item,
                    base_by_id[int(item["id"])],
                    args.mode,
                    args.base_tail_chars,
                    candidate_results,
                    args.candidate_tail_chars,
                ),
                disable_thinking=args.disable_thinking,
            )
            for item in batch
        ]
        if args.boxed_prefill:
            prompts = [apply_boxed_prefill(prompt) for prompt in prompts]

        if args.backend == "vllm":
            outputs = model.generate(prompts, sampling_params, lora_request=lora_request)
            raw_responses = [output.outputs[0].text.strip() for output in outputs]
        else:
            encoded = tokenizer(
                prompts,
                return_tensors="pt",
                padding=True,
                truncation=True,
                max_length=args.max_model_len,
            ).to(model.device)
            import torch

            with torch.no_grad():
                output_ids = model.generate(
                    **encoded,
                    max_new_tokens=args.max_tokens,
                    do_sample=False,
                    pad_token_id=tokenizer.pad_token_id,
                )
            raw_responses = [
                tokenizer.decode(
                    output[encoded["input_ids"].shape[1] :],
                    skip_special_tokens=True,
                ).strip()
                for output in output_ids
            ]
        if args.boxed_prefill:
            raw_responses = [restore_boxed_prefill(response) for response in raw_responses]

        for item, raw_response in zip(batch, raw_responses):
            base_row = base_by_id[int(item["id"])]
            all_rows.append(
                {
                    "id": item["id"],
                    "is_mcq": bool(item.get("options")),
                    "response": coerce_boxed_response(raw_response),
                    "raw_qwen_response": raw_response,
                    "base_response": base_row.get("response", ""),
                    "qwen_pass_mode": args.mode,
                    "qwen_model": args.model,
                    "qwen_candidate_sources": sorted(candidate_results),
                    "disable_thinking": args.disable_thinking,
                    "boxed_prefill": args.boxed_prefill,
                    "legal_note": (
                        "Qwen-only generation pass; no solver, tool, API, "
                        "calculator, or deterministic answer computation."
                    ),
                }
            )
        write_jsonl(args.output, sorted(all_rows, key=lambda row: int(row["id"])))

    print(
        json.dumps(
            {
                "kaggle_upload": "not_performed",
                "output": str(args.output),
                "rows": len(all_rows),
                "model": args.model,
                "mode": args.mode,
                "disable_thinking": args.disable_thinking,
                "boxed_prefill": args.boxed_prefill,
                "compliance_mode": "legal_qwen_model_only_boxed_pass",
            },
            indent=2,
        ),
        flush=True,
    )
    return 0


