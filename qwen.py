    parser.add_argument("--backend", choices=["transformers", "vllm"], default="vllm")
    parser.add_argument("--data", type=Path, default=ROOT / "kaggle_data/private.jsonl")
    parser.add_argument("--base-results", type=Path, required=True)
    parser.add_argument("--output", type=Path, required=True)
    parser.add_argument("--question-filter", choices=["all", "mcq", "freeform"], default="all")
    parser.add_argument("--ids", default="")
    parser.add_argument("--ids-file", type=Path)
    parser.add_argument("--start-index", type=int, default=0)
    parser.add_argument("--limit", type=int, default=0)
    parser.add_argument("--batch-size", type=int, default=24)
    parser.add_argument("--max-tokens", type=int, default=256)
    parser.add_argument("--max-model-len", type=int, default=8192)
    parser.add_argument("--dtype", default="auto")
